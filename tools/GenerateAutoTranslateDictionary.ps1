$builder = [System.Text.StringBuilder]::new()
[void] $builder.AppendLine("// <auto-generated />")
[void] $builder.AppendLine("using System.Collections.Generic;")
[void] $builder.AppendLine()
[void] $builder.AppendLine("namespace IronworksTranslator.Services.FFXIV")
[void] $builder.AppendLine("{")
[void] $builder.AppendLine("    internal static partial class AutoTranslateDictionary")
[void] $builder.AppendLine("    {")

foreach ($language in $languages) {
    $csvPath = Join-Path $DataminingCsvRoot $language.Path
    if (!(Test-Path -LiteralPath $csvPath)) {
        throw "Completion CSV not found: $csvPath"
    }

    [void] $builder.AppendLine("        private static readonly IReadOnlyDictionary<ulong, string> $($language.Field) =")
    [void] $builder.AppendLine("            new Dictionary<ulong, string>")
    [void] $builder.AppendLine("            {")

    $rows = Get-Content -LiteralPath $csvPath -Encoding UTF8 |
        Select-Object -Skip 1 |
        ConvertFrom-Csv -Header $headers

    foreach ($row in $rows) {
        $key = [int] $row.Key
        if ($key -eq 0) {
            continue
        }

        $payloadKey = ConvertTo-PayloadKey -Group ([int] $row.Group) -RowId ([int] $row.RowId)
        $text = Format-CSharpString -Value $row.Text
        [void] $builder.AppendLine(("                [0x{0:X}UL] = {1}," -f $payloadKey, $text))
    }

    [void] $builder.AppendLine("            };")
    [void] $builder.AppendLine()
}

[void] $builder.AppendLine("    }")
[void] $builder.AppendLine("}")

$resolvedOutput = $ExecutionContext.SessionState.Path.GetUnresolvedProviderPathFromPSPath($OutputPath)
$outputDirectory = Split-Path -Parent $resolvedOutput
New-Item -ItemType Directory -Force -Path $outputDirectory | Out-Null
Set-Content -LiteralPath $resolvedOutput -Value $builder.ToString() -Encoding UTF8
