function ConvertTo-CompletionNumberBytes {
    param([int] $RowId)

    $completionNumber = $RowId + 1
    if ($completionNumber -le 0xCF) {
        return @([byte] $completionNumber)
    }

    $value = $completionNumber - 1
    if ($value -le 0xFF) {
        return @([byte] 0xF0, [byte] $value)
    }

    if ($value -le 0xFFFF) {
        return @(
            [byte] 0xF2,
            [byte] (($value -shr 8) -band 0xFF),
            [byte] ($value -band 0xFF)
        )
    }

    if ($value -le 0xFFFFFF) {
        return @(
            [byte] 0xF4,
            [byte] (($value -shr 16) -band 0xFF),
            [byte] (($value -shr 8) -band 0xFF),
            [byte] ($value -band 0xFF)
        )
    }

    return @(
        [byte] 0xF6,
        [byte] (($value -shr 24) -band 0xFF),
        [byte] (($value -shr 16) -band 0xFF),
        [byte] (($value -shr 8) -band 0xFF),
        [byte] ($value -band 0xFF)
    )
}

function ConvertTo-PayloadKey {
    param(
        [int] $Group,
        [int] $RowId
    )

    if ($Group -lt 0 -or $Group -gt 0xFF) {
        throw "Completion group is out of byte range: $Group"
    }

    $payload = @([byte] $Group) + (ConvertTo-CompletionNumberBytes -RowId $RowId)
    [UInt64] $key = 0
    foreach ($byte in $payload) {
        $key = ($key -shl 8) -bor [UInt64] $byte
    }

    return $key
}

function Format-CSharpString {
    param([string] $Value)

    $escaped = [System.Text.StringBuilder]::new()
    [void] $escaped.Append('"')
    foreach ($character in $Value.ToCharArray()) {
        $code = [int] $character
        switch ($character) {
            '"' { [void] $escaped.Append('\"'); break }
            '\' { [void] $escaped.Append('\\'); break }
            "`r" { [void] $escaped.Append('\r'); break }
            "`n" { [void] $escaped.Append('\n'); break }
            "`t" { [void] $escaped.Append('\t'); break }
            default {
                if ($code -lt 0x20 -or $code -gt 0x7E) {
                    [void] $escaped.Append(('\u{0:X4}' -f $code))
                }
                else {
                    [void] $escaped.Append($character)
                }
            }
        }
    }

    [void] $escaped.Append('"')
    return $escaped.ToString()
}

function Assert-Key {
    param(
        [int] $Group,
        [int] $RowId,
        [UInt64] $Expected
    )

    $actual = ConvertTo-PayloadKey -Group $Group -RowId $RowId
    if ($actual -ne $Expected) {
        throw ("Payload key mismatch for group {0}, row {1}: expected 0x{2:X}, got 0x{3:X}" -f $Group, $RowId, $Expected, $actual)
    }
}

